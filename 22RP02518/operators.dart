void main() {
  int a = 10;
  int b = 3;

  print('Add: ${a + b}');
  print('Subtract: ${a - b}');
  print('Multiply: ${a * b}');
  print('Divide: ${a / b}');
  print('Modulus: ${a % b}');

  print('Is Equal? ${a == b}');
  print('Is Not Equal? ${a != b}');
  print('Is Greater? ${a > b}');

  bool x = true;
  bool y = false;
  print('AND: ${x && y}');
  print('OR: ${x || y}');
  print('NOT x: ${!x}');
}
