//A variable is a named storage for data. It allows you to store values like numbers, text, and booleans and reuse or change them later in your code.
void main() {
  var name = 'Daniel';       // String
  int age = 23;              // Integer
  double height = 5.9;       // Decimal number
  bool isStudent = true;     // Boolean (true/false)

  print('Name: $name');
  print('Age: $age');
  print('Height: $height');
  print('Is a Student: $isStudent');
}