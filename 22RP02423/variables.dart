void main() {
  //  String - text
  String name = "Philemon";

  // int - whole numbers
  int age = 22;

  //  double - decimal numbers
  double gpa = 4.0;

  //  bool - true/false
  bool isStudent = true;

  //  var - type inferred automatically
  var school = "Tumba College of Technology";

  //  final - value set once, at runtime
  final registrationDate = DateTime.now();

  //  const - fixed value at compile time
  const country = "Rwanda";

  //  Displaying all variables
  print("Name: $name");
  print("Age: $age");
  print("GPA: $gpa");
  print("Is student: $isStudent");
  print("School: $school");
  print("Registered on: $registrationDate");
  print("Country: $country");
}
