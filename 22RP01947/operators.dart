void main() {
  int x = 15;
  int y = 5;

  print("Addition: ${x + y}");
  print("Subtraction: ${x - y}");
  print("Multiplication: ${x * y}");
  print("Division: ${x / y}");
  print("Modulus: ${x % y}");

  
  bool isTrue = true;
  bool isFalse = false;
  print("AND: ${isTrue && isFalse}");
  print("OR: ${isTrue || isFalse}");
  print("NOT: ${!isTrue}");
}
